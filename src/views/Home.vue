<script setup lang="ts">
import { invoke } from '@tauri-apps/api/tauri'
import { ref, onMounted } from 'vue'
import { flattenSettings, RobloxSettingsValues } from '../fflags'

import { animateSpin, patch } from '../lib'

async function launchRoblox() {
    animateSpin('launch-roblox-svg', 1000);

    try {
        const res = await invoke('get_cache', {cacheFileName: 'robloxsettings.json'});
        
        if (typeof res !== 'object' || res == null) {
            console.log('No cache file in sight');
            const launchRes = await invoke('launch_roblox');
            console.log(launchRes);
            return;
        }


        await patch(res);

        await invoke('launch_roblox');
    } catch (error) {
        console.error('Error launching Roblox:', error);
    }
}

// Version
let version = ref('unknown')

invoke('get_version').then((res) => {
    version.value = res as string
})

// Dark mode

var theme = ref(document.documentElement.getAttribute('data-theme') || 'light')

function toggleTheme() {
    if (theme.value === 'light') {
        theme.value = 'dark'
        document.documentElement.setAttribute('data-theme', 'dark')
        window.localStorage.setItem('theme', 'dark')
    } else {
        theme.value = 'light'
        document.documentElement.setAttribute('data-theme', 'light')
        window.localStorage.setItem('theme', 'light')
    }

    invoke('edit_config', { path: 'theme', value: theme.value })

    animateSpin('theme-toggle-svg', 500)
}
</script>

